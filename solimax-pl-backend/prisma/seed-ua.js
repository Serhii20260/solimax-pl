async function main() {
  console.log("Seeding Ukrainian knowledge base...");

  for (const item of UA_KNOWLEDGE_ITEMS) {
    await prisma.knowledgeItem.create({ data: item });
    console.log(`  ✓ ${item.title}`);
  }

  console.log(`\n✅ Seeded ${UA_KNOWLEDGE_ITEMS.length} Ukrainian items`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
